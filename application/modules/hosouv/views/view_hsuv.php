                    url += '/nganhnghe:' + fields[$('#ResumeSeekerbranch').val()];
                }

                if ($('#ResumeSeekerworkplace').val() != '') {
                    url += '/tinhthanhpho:' + provinces[$('#ResumeSeekerworkplace').val()];
                }

                if ($('#ResumeSeekergender').val() != '') {
                    url += '/gioitinh:' + genders[$('#ResumeSeekergender').val()];
                }

                if ($('#ResumeSeekerfunction').val() != '') {
                    url += '/vitri:' + positions[$('#ResumeSeekerfunction').val()];
                }

                if ($('#ResumeSeekerlevel').val() != '') {
                    url += '/trinhdo:' + levels[$('#ResumeSeekerlevel').val()];
                }

                if ($('#ResumeSeekerexperience').val() != '') {
                    url += '/kinhnghiem:' + experiences[$('#ResumeSeekerexperience').val()];
                }

                if ($('#ResumeLanguage').val() != '') {
                    url += '/ngoaingu:' + languages[$('#ResumeLanguage').val()];
                }

                if ($('#ResumeSeekersalaryunit').val() != '') {
                    if ($('#ResumeSeekersalaryunit').val() == 'Y') {
                        if ($('#resume-salary-vnd').val() != '') {
                            url += '/mucluong:' + salary_vnd[$('#resume-salary-vnd').val()];
                        }
                    } else {
                        if ($('#resume-salary-usd').val() != '') {
                            url += '/mucluong:' + salary_usd[$('#resume-salary-usd').val()];
                        }
                    }
                }

                if (url != '') {
                    url = url_root + url;
                    window.location.href = url;
                }

                return false;
            }


